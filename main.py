@bot.command(aliases=['coins', 'bal', 'فلوس'])
async def balance(ctx, member: discord.Member = None):
    m = member or ctx.author
    d = user_data.get(str(m.id), {'coins': 0})
    await ctx.send(f"💰 رصيد **{m.display_name}**: `{d['coins']}` كوينز")

